package Programs;

public class OddOrEvenProgram {
	public static void main(String[] args) {
		
		
		// to Print the odd / Even no  up 1 to 30;
		
		for(int i = 1; i<=30; i++) {
			
			if(i%2==0) {
				System.out.println(" Even no ="+ i);
			}else {
				System.out.println(" Odd No = "+ i);
			}
			
			
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
